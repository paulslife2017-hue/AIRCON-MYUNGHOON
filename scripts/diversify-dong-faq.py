# ── 동 페이지 파일 목록 ───────────────────────────────────────────
files = os.listdir(AREA_DIR)
three_files = []
for f in files:
    dec = urllib.parse.unquote(f.replace('.html', ''))
    if dec.count('-') == 2:
        three_files.append(f)

print(f"동 페이지 총: {len(three_files)}개")

updated = 0
for i, fname in enumerate(sorted(three_files)):
    fpath = os.path.join(AREA_DIR, fname)
    with open(fpath, 'r', encoding='utf-8') as fp:
        content = fp.read()

    dec   = urllib.parse.unquote(fname.replace('.html', ''))
    parts = dec.split('-')
    region, dong, service = parts[0], parts[1], parts[2]

    # FAQ 세트 순환 (지역+서비스 해시 기반)
    set_idx = (hash(dec) % len(FAQ_SETS))
    faq_set = FAQ_SETS[set_idx]

    # 새로운 FAQPage JSON-LD 생성 (동 이름 치환)
    new_faq_entities = []
    for qa in faq_set:
        q_text = qa['name'].replace('에어컨', f'{dong} 에어컨') if i % 3 == 0 else qa['name']
        new_faq_entities.append({
            "@type": "Question",
            "name": q_text,
            "acceptedAnswer": {"@type": "Answer", "text": qa['text']}
        })

    new_faq_ld = {
        "@context": "https://schema.org",
        "@type": "FAQPage",
        "mainEntity": new_faq_entities
    }

    new_faq_block = f'<script type="application/ld+json">\n  {json.dumps(new_faq_ld, ensure_ascii=False, indent=2)}\n  </script>'

    # 기존 FAQPage 블록 교체
    # 패턴: <script type="application/ld+json"> ... FAQPage ... </script>
    def replace_faq(content, new_block):
        pattern = r'<script type="application/ld\+json">\s*\{[^<]*"FAQPage"[^<]*(?:\n[^<]*)*\}</script>'
        # 더 안전한 방식으로 처리
        blocks = re.findall(r'<script type="application/ld\+json">(.*?)</script>', content, re.DOTALL)
        for block in blocks:
            try:
                d = json.loads(block.strip())
                if d.get('@type') == 'FAQPage':
                    old_tag = f'<script type="application/ld+json">{block}</script>'
                    content = content.replace(old_tag, new_block, 1)
                    return content, True
            except:
                pass
        return content, False

    new_content, replaced = replace_faq(content, new_faq_block)
    if replaced:
        with open(fpath, 'w', encoding='utf-8') as fp:
            fp.write(new_content)
        updated += 1

print(f"FAQ 다양화 완료: {updated}개 페이지 업데이트")
